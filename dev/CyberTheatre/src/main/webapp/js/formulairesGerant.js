function afficherFormulaireSpectacle() {
  var div = document.getElementById("formulaireSpectacle");
  if (div.style.display === "block") {
    div.style.display = "none";
  } else {
    div.style.display = "block";
  }
}

function afficherFormulaireRepresentation() {
  var div = document.getElementById("formulaireRepresentation");
  if (div.style.display === "block") {
    div.style.display = "none";
  } else {
    div.style.display = "block";
  }
}

